                    <h2><i class="fa-solid fa-angles-right" style="color: #000000;"></i> Helado de café con mendigos
                    </h2>
                    <hr>
                    <h3>Ingredientes</h3>
                    <p>Para el helado:<br>
                        3 dl de nata<br>
                        2 dl de leche<br>
                        2 cuch. de café soluble<br>
                        150 g de azúcar<br>
                        1 pizca de cacao en polvo<br>
                        2 dl de café<br>
                        Para los mendigos:<br>
                        sésamo<br>
                        150 g de chocolate<br>
                        1 mandarina<br>
                        almendras<br>
                        pasas<br>
                        avellanas<br></p>
                    <hr>
                    <h3>Preparación</h3>
                    <p>Congelar el café en una cubitera. Hay que hacerlo muy concentrado para que tenga sabor.<br>
                        En el vaso americano, triturar los cubitos con la mezcla de la leche, la nata, el azúcar, la
                        canela y el cacao en polvo.<br>
                        Para los mendigos, derretir el chocolate en el micro y disponer círculos con el reverso de una
                        cuchara sobre el papel de horno.<br>
                        Antes de que se endurezca, poner ralladura de mandarina, sésamo, y repartir trocitos de los
                        frutos secos.<br>
                        Dejar enfriar en la nevera.<br>
                        Repartir el helado en vasitos y coronar con una teja de chocolate y frutos secos.<br>
                        Terminar con menta fresca.<br></p>
