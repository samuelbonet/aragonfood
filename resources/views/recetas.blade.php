                    <h2><i class="fa-solid fa-angles-right" style="color: #000000;"></i> Pizza sin gluten</h2>
                    <hr>
                    <h3>Ingredientes</h3>
                    <p>150 g de almidón de maíz <br>
                        95 g de harina de trigo sarraceno<br>
                        25 g de harina de avena (Puede sustituirse por más sarraceno o incluso harina de arroz) <br>
                        8 g de psyllium <br>
                        10 g de lino molido <br>
                        300 ml de agua <br>
                        50 g de patata cocida (opcional) <br>
                        0,5 g de levadura deshidratada <br>
                        6 g de sal <br>
                        3 g de xantana <br>
                        20 ml de AOVE <br></p>
                    <hr>
                    <h3>Preparación</h3>
                    <p>En un cuenco, batiremos con una batidora el agua, la levadura, el psyllium, el lino y la patata
                        cocida hasta que se hidraten los mucilagos y obtengamos un líquido espeso.<br>
                        Echaremos las harinas con la sal y amasaremos. Una vez consigamos una masa uniforme, mezclaremos
                        la xantana con el aceite de oliva para añadirlo a la mezcla anterior,seguiremos amasando y
                        reposaremos la masa 30 min bien tapada.<br>
                        Dividiremos la masa, en forma de bola y dejaremos que fermenten de 12 a 24 horas en la nevera
                        (tapada con papel film).<br>
                        Pasado este tiempo estiraremos la masa con la ayuda de las manos presionando desde el centro
                        hasta los bordes y añadiremos los ingredientes que prefiramos.<br>
                        Una vez tengamos la pizza completa la hornearemos a 250ºC en 15 minutos.<br></p>
